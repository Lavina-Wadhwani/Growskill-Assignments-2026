package assignment2;

public class asciicodeAssignment {

	public static void main(String[] args) {
		
		char ch='A';
		int value = ch;
		System.out.println("ASCII Value of " + ch +" = "+ value);
		
		char ch1='a';
		int value1 = ch1;
		System.out.println("ASCII Value of " + ch1 +" = "+ value1);
		
		char ch2='0';
		int value2 = ch2;
		System.out.println("ASCII Value of " + ch2 +" = "+ value2);
		
		char ch3='@';
		int value3 = ch3;
		System.out.println("ASCII Value of " + ch3 +" = "+ value3);
		
	}
}
